import {connect} from 'react-redux';
import SearchBar from './search_bar';
import {receiveSearchBool, searchDatabase} from '../../actions/search-actions';


const mapStateToProps = (state) => {
  const songIds = state.search.songs;
  const albumIds = state.entities.albums;
  return {
    songIds,
    albumIds,
    statusOfModal: state.search.searchBarStatus
  };
};

const mapDispatchToProps = (dispatch) => {
  return {
    receiveSearchBool: () => dispatch(receiveSearchBool()),
    searchDatabase: (title) => dispatch(searchDatabase(title))
  };
};
